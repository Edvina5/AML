package Util;

public class Customer {
	
	private int cid;
	private int sf;
	private int rf;
	private float sa;
	private float ra;
	private String date;
	
	
	public int getCID(){
		return cid;
	}
	
	public void setCID(int cid){
		this.cid = cid;
	}
	
	public int getSF(){
		return sf;
	}
	
	public void setSF(int sf){
		this.sf = sf;
	}
	
	public int getRF(){
		return rf;
	}
	
	public void setRF(int rf){
		this.rf = rf;
	}
	
	public float getSA(){
		return sa;
	}
	
	public void setSA(float sa){
		this.sa = sa;
	}
	
	public float getRA(){
		return ra;
	}
	
	public void setRA(float ra){
		this.ra = ra;
	}
	
	public String getDate(){
		return date;
	}
	
	public void setDate(String date){
		this.date = date;
	}
	
	


}
